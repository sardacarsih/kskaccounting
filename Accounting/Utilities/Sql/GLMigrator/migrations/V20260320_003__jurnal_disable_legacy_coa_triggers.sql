-- Purpose: Disable legacy COA mutation triggers on ACCT_JURNAL_DTL.
-- Date: 2026-03-20

SET SERVEROUTPUT ON;

BEGIN
    EXECUTE IMMEDIATE 'ALTER TRIGGER UPDATE_COA_FROM_UPDATE DISABLE';
    DBMS_OUTPUT.PUT_LINE('DISABLED TRIGGER: UPDATE_COA_FROM_UPDATE');
EXCEPTION
    WHEN OTHERS THEN
        DBMS_OUTPUT.PUT_LINE('SKIPPED/ERROR UPDATE_COA_FROM_UPDATE: ' || SQLERRM);
END;
/

BEGIN
    EXECUTE IMMEDIATE 'ALTER TRIGGER UPDATE_COA_FROM_DELETE DISABLE';
    DBMS_OUTPUT.PUT_LINE('DISABLED TRIGGER: UPDATE_COA_FROM_DELETE');
EXCEPTION
    WHEN OTHERS THEN
        DBMS_OUTPUT.PUT_LINE('SKIPPED/ERROR UPDATE_COA_FROM_DELETE: ' || SQLERRM);
END;
/
